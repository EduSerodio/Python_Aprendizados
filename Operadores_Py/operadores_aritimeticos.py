# INTRODUÇÃO: Os operadores aritiméticos executam operações matemáticas, como adição, subtração com operandos.

# Adição
print(1 + 1)

# Subtração 
print(1 - 10)

# Multiplicação 
print(5 * 15)

# Divisão 
print(10 / 2)

# Divisão inteira
print(10 // 2)

# Módulo 
print(10 % 3)

# Exponenciação
print(2 ** 3)

# PRECEDÊNCIA DE OPERADORES : Na matemática existe uma regra que indica quais operações devem ser executados primeiro. 
# Isso é útil pois ao analisar uma expressão, a depender da ordem das operações o valor pode ser diferente.

# A definição indica a seguinte ordem como a correta:
# - Parêntesis
# - Expoêntes
# - Multiplicações e divisões (da esquerda para a direita)
# - Somas e subtrações (da esqeurda para a direita)

# EXEMPLOS:

print(10 - 5 * 2)     # Aqui o retorno será 0

print((10 - 5) * 2)   #Aqui o retorno será 10

print(10 ** 2 * 2)    #Aqui o retorno será 200

print(10 ** (2 * 2))  #Aqui o retorno será 10000

print(10 / 2 * 4)     #Aqui o retorno será 20.0



