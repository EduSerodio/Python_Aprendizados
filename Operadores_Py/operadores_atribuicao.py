# INTRODUÇÃO: São operadores ultilizados para definir o valor incial ou sobrescrever o valor de uma variável

# ATRIBUIÇÃO SIMPLES

saldo = 500
print(saldo)

# ATRIBUIÇÃO COM ADIÇÃO

saldo = 500
saldo += 200 #Aqui podemos passar a adição logo na montagem da variavel  ultilizando "+="
print(saldo)

# ATRIBUIÇÃO COM SUBTRAÇÃO 

saldo = 500
saldo -= 100 #Aqui podemos passar a subtração logo na montagem da variavel  ultilizando "-="
print(saldo)


# ATRIBUIÇÃO COM MULTIPLICAÇÃO 

saldo = 500
saldo *= 2 #Aqui podemos passar a multiplicação logo na montagem da variavel  ultilizando "*="
print(saldo)

# ATRIBUIÇÃO COM DIVISÃO

saldo = 500
saldo /= 5 #Aqui podemos passar a divisao logo na montagem da variavel  ultilizando "/=" para retornar o float
print(saldo)

saldo = 500
saldo //= 5 #Aqui podemos passar a divisao logo na montagem da variavel  ultilizando "//=" para retornar um inteiro
print(saldo)

# ATRIBUIÇÃO COM MÓDULO

saldo = 500
saldo %= 480
print(saldo)

# ATRIBUIÇÃO COM EXPONENCIAÇÃO

saldo = 80
saldo **= 2
print(saldo)