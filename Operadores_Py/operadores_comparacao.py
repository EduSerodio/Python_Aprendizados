# INTRODUÇÃO : São operadores ultilizados para comparar dois valores

# IGUALDADE

saldo = 450
saque = 200

print(saldo == saque) #Nesse caso ele vai comparar se um é maior que o outro e retornar um valor booleano que nesse exemple é "false"

# DIFERENÇA

saldo = 450
saque = 200

print(saldo != saque) #Nesse caso retorna o valor booleano "True"

# MAIOR QUE / MAIOR OU IGUAL

saldo = 450
saque = 200

print(saldo > saque) # O retorno seria booleano "true"

print(saldo >= saque) # O retorno sera boolenao "true"

# MENOR QUE / MENOR OU IGUAL

saldo = 450
saque = 200

print(saldo < saque) # O retorno seria booleano "fALSE"

print(saldo <= saque) # O retorno sera boolenao "FALSE"