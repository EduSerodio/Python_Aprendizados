# ------------------------------------- INTRPDUÇÃO --------------------------------------
# Tuplas são estruturas de dados muito parecidas com as listas, a principal diferença é 
# que as tuplas são imutáveis enquanto lista são mutáveis. Podemos criar tuplas da classe
# tuple, ou colocando valores separados por vírgula de parenteses.

# -------------------------------------- EXEMPLO ----------------------------------------

frutas = ("laranja", "pera", "uva",)

letras = tuple("python")

numeros = tuple([1, 2, 3, 4])

pais = ("Brasil",)
