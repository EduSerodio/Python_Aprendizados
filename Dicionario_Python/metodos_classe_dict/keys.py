# {}.KEYS - Retorna as chaves que temos disponiveis no dicionários, usando assim 
# quando queremos saber quantas chaves um dicionário tem 

# -----------------------------------Exemplos------------------------------------------
contatos = {
    "eduardo@gamil.com" : {"nome": "Eduardo", "idade": 23, "telefone": "1234-5678"},
}

print(contatos.keys())