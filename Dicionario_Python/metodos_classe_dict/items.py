# ---------------------------------INTRODUÇÃO----------------------------------------
# {}.ITEMS - Retorna uma lista de tuplas, muito util quando usamos o for e você quer
# iterar sobre esses valores do dicionário

# -----------------------------------Exemplos------------------------------------------
contatos = {
    "eduardo@gamil.com" : {"nome": "Eduardo", "idade": 23, "telefone": "1234-5678"},
}

print(contatos.items())