# ---------------------------------INTRODUÇÃO----------------------------------------
# Um dicionário é um conjunto não-ordenado de pares chave:valor, onde as chaves são 
# únicas em uma dada instância do dicionário. Dicionário são delimitados por chaves:
# {}, e contém uma lista de pares chave:valor separados por vírgulas.

# -----------------------------------Exemplos------------------------------------------

pessoa = {"nome": "Guilherme", "idade": 28}

pessoa = dict(nome="Guilherme", idade=28)

pessoa["telefone"] = "3333-1234"