# ----------------------------------INTRODUÇÃO--------------------------------------
# {}.DISCARD - Serve para descartarmos algum valor que queremos, e não retorna erro
# 

# ----------------------------------EXEMPLO------------------------------------------

numeros = {1, 2, 3, 1, 2, 4, 5, 5, 6, 7, 8, 9, 0}

print(numeros)
numeros.discard(1)
print(numeros)
numeros.discard(45)
print(numeros)
