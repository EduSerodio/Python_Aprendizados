# {}.REMOVE - Serve para remover elementos dentro do set, mas caso o elemento que 
# selecionarmos nao existir, retornará um erro para nós.

# ----------------------------------EXEMPLO------------------------------------------

numeros = {1, 2, 3, 1, 2, 4, 5, 5, 6, 7, 8, 9, 0}

print(numeros)
numeros.remove(0)
print(numeros)
